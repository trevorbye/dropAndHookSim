public class RunUtilityEntity {
    private long runNumber;
    private double umulativeAverage;

    public RunUtilityEntity() {
    }

    public long getRunNumber() {
        return runNumber;
    }

    public void setRunNumber(long runNumber) {
        this.runNumber = runNumber;
    }

    public double getUmulativeAverage() {
        return umulativeAverage;
    }

    public void setUmulativeAverage(double umulativeAverage) {
        this.umulativeAverage = umulativeAverage;
    }
}
